var name = prompt("What is your name?");
var age = prompt("What is your age?");

if (age >= 50)
{
	alert("Hello " + name + ". You are " + age + " years old. Unless you live to be more than 100, you have passed the halfway mark. Congratulations!") 
}
else
{
	alert("Hello " + name + ". You are " + age + " years old. Enjoy your youthful enthusiasm.")
}